//&&   ||     !=

let isLogin = false;
let isRegister = true;

if (isLogin && isRegister) {
    console.log(true);
} else {
    console.log(false);
}

if (isLogin || isRegister) {
    console.log(true);
} else {
    console.log(false);
}

if (isLogin != isRegister) {
    console.log(true);
} else {
    console.log(false);
}

if (isLogin == isRegister) {
    console.log(true);
} else {
    console.log(false);
}